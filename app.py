def init_db():
    if PracticalWork.query.first() is None:
        for w in FULL_COURSE_DATA:
            data = w.copy()
            sections = data.pop("sections")
            new_work = PracticalWork(**data, sections_json=json.dumps(sections))
            db.session.add(new_work)
        db.session.commit()


@app.route("/")
def index():
    works = PracticalWork.query.all()
    return render_template(
        "index.html", title="Мультимедийное пособие по VR", works=works
    )


@app.route("/practice/<int:work_id>")
def practice(work_id):
    work = PracticalWork.query.get_or_404(work_id)
    sections = json.loads(work.sections_json)
    return render_template("practice.html", work=work, sections=sections)


@app.route("/api/complete/<int:work_id>", methods=["POST"])
def complete_work(work_id):
    work = PracticalWork.query.get_or_404(work_id)
    work.is_completed = True
    db.session.commit()
    return jsonify({"status": "success"})


@app.route("/api/reset", methods=["POST"])
def reset_progress():
    PracticalWork.query.update({PracticalWork.is_completed: False})
    db.session.commit()
    return jsonify({"status": "success"})


if __name__ == "__main__":
    with app.app_context():
        db.create_all()
        init_db()
    app.run(host="0.0.0.0", port=3000, debug=True)
